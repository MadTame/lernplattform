package de.fh.albsig.id92012.data;

/**
 * Represents the roles available for a user in the application.
 * 
 * <p>This enum defines the two roles: {@link Role#USER} and {@link Role#ADMIN}.</p>
 * <ul>
 * <li>{@link Role#USER}: A regular user with basic access rights.</li>
 * <li>{@link Role#ADMIN}: An administrator with elevated privileges.</li>
 * </ul>
 */
public enum Role {
  USER, ADMIN;
}
